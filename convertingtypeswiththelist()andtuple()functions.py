# Converting a list to a tuple
example_tuple = tuple(['cat', 'dog', 5])
print(example_tuple)
# Output: ('cat', 'dog', 5)

# Converting a tuple to a list
example_list = list(('cat', 'dog', 5))
print(example_list)
# Output: ['cat', 'dog', 5]

# Converting a string to a list of characters
string_list = list('hello')
print(string_list)
# Output: ['h', 'e', 'l', 'l', 'o']
